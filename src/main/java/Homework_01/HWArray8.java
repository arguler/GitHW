package Homework_01;
//Create an array to store char values and then print those in reverse order
public class HWArray8 {
    public static void main(String[] args) {
        char[] letters={'e','k','i','l','e','M'};
        for (int i = 5; i >= 0; i--) {
            System.out.print(letters[i]);
        }
    }
}
