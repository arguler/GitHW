package Replit;

public class repl_178_EncapsulatonDemo {
    private String empName;
    private int empAge;

    String getEmpName(){
        return empName;
    }
    public void setEmpName(String newempName){
        this.empName=newempName;
    }
    int getEmpAge(){
        return empAge;
    }
    public void setEmpAge(int newempAge){
        this.empAge= newempAge;
    }
}
